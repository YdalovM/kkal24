/**
 * Ориентиры калорийности и БЖУ для справочных страниц и калькулятора питания.
 * Все значения приблизительные и даны на 100 г продукта в указанном виде
 * (для напитков — на 100 мл), если не оговорено иначе.
 *
 * Сверка: для базовых продуктов ориентируемся на USDA FoodData Central / FDC
 * и типичные справочники по составу продуктов. Для части блюд и напитков
 * возможен заметный разброс из-за рецепта, бренда и технологии приготовления.
 */
export type FoodCalorieRow = {
  name: string;
  /** Килокалории на 100 г (или на 100 мл для жидкостей). */
  kcal100: number;
  /** Белки на 100 г/100 мл. */
  protein100: number;
  /** Жиры на 100 г/100 мл. */
  fat100: number;
  /** Углеводы на 100 г/100 мл. */
  carbs100: number;
  note?: string;
};

export type FoodCalorieCategory = {
  id: string;
  title: string;
  intro?: string;
  /** По умолчанию ккал на 100 г продукта; для напитков — на 100 мл. */
  kcalUnit?: "per100g" | "per100ml";
  rows: readonly FoodCalorieRow[];
};

function foodRow(
  name: string,
  kcal100: number,
  protein100: number,
  fat100: number,
  carbs100: number,
  note?: string,
): FoodCalorieRow {
  return { name, kcal100, protein100, fat100, carbs100, note };
}

